use chess::{Color, Piece, Square};

pub fn piece_square_table(piece: Piece, square: Square, piece_color: Color) -> i64 {
    let square_index = square.to_index();

    let lookup_index = match piece_color {
        Color::White => square_index,
        Color::Black => 63 - square_index,
    };

    let bonus = match piece {
        Piece::Pawn => PAWNS_TABLE[lookup_index],
        Piece::Knight => 0,
        Piece::Bishop => 0,
        Piece::Rook => 0,
        Piece::Queen => 0,
        Piece::King => 0,
    } as i64;
    bonus
}

